public class Patterns {
    public static void main(String[] argv) {
        // Initial testing
        System.out.println("Initial testing");
        System.out.println("--*--");
        System.out.print("\n");

        // Diamond of stars
        System.out.println("Diamond of stars");
        System.out.println("--*--");
        System.out.println("-*-*-");
        System.out.println("*---*");
        System.out.println("-*-*-");
        System.out.println("--*--");
        System.out.print("\n");

        // Diamond of circles
        System.out.println("Diamond of circles");
        System.out.println("--o--");
        System.out.println("-o-o-");
        System.out.println("o---o");
        System.out.println("-o-o-");
        System.out.println("--o--");
        System.out.print("\n");
    }
}